    private void btnVerDetalleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerDetalleActionPerformed

        if (idPedidoSeleccionado == 0) {
            JOptionPane.showMessageDialog(this, "Seleccione un pedido de la tabla.");
            return;
        }

        List<Object[]> detalle = controller.listarDetallePorPedido(idPedidoSeleccionado);

        DefaultTableModel modelo = new DefaultTableModel(new Object[]{"IdDetalle", "Producto", "Cantidad", "Precio Unit."}, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        for (Object[] fila : detalle) {
            modelo.addRow(fila);
        }

        javax.swing.JTable tabla = new javax.swing.JTable(modelo);
        javax.swing.JScrollPane scroll = new javax.swing.JScrollPane(tabla);
        scroll.setPreferredSize(new java.awt.Dimension(500, 200));

        JOptionPane.showMessageDialog(this, scroll, "Detalle del Pedido #" + idPedidoSeleccionado, JOptionPane.PLAIN_MESSAGE);

    }//GEN-LAST:event_btnVerDetalleActionPerformed

    private void btnEliminarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarPedidoActionPerformed

        if (idPedidoSeleccionado == 0) {
            JOptionPane.showMessageDialog(this, "Seleccione un pedido de la tabla.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                "¿Está seguro de eliminar este pedido y todas sus líneas?", "Confirmar",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            boolean respuesta = controller.eliminarPedido(idPedidoSeleccionado);

            if (respuesta) {
                JOptionPane.showMessageDialog(this, "Pedido eliminado correctamente.");
                idPedidoSeleccionado = 0;
                listarPedidos();
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar el pedido.");
            }
        }

    }//GEN-LAST:event_btnEliminarPedidoActionPerformed

    private void tblPedidosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPedidosMouseClicked

        int fila = tblPedidos.getSelectedRow();

        if (fila != -1) {
            idPedidoSeleccionado = (int) tblPedidos.getValueAt(fila, 0);
        }

    }//GEN-LAST:event_tblPedidosMouseClicked

    private void cargarClientes() {
        cbCliente.removeAllItems();
        List<Cliente> lista = clienteController.listarClientes();
        for (Cliente c : lista) {
            cbCliente.addItem(c.getIdCliente() + " - " + c.getNombre());
        }
    }

    private void cargarProductos() {
        cbProducto.removeAllItems();
        productos = productoController.listarProductos();
        for (Producto p : productos) {
            cbProducto.addItem(p.getCodProducto());
        }
    }

    private void listarPedidos() {
        DefaultTableModel modelo = (DefaultTableModel) tblPedidos.getModel();
        modelo.setRowCount(0);

        List<Object[]> lista = controller.listarPedidos();
        for (Object[] fila : lista) {
            modelo.addRow(fila);
        }
    }

    private void actualizarMontoTotal() {
        double total = 0;
        for (DetallePedido d : lineasPendientes) {
            total += d.getSubtotal();
        }
        lblMontoTotal.setText(String.format("Monto total: S/ %.2f", total));
    }

    private void limpiarFormulario() {
        lineasPendientes.clear();
        ((DefaultTableModel) tblDetalleNuevo.getModel()).setRowCount(0);
        actualizarMontoTotal();
        txtFecha.setText(LocalDate.now().toString());
        txtCantidadLinea.setText("");
        if (cbCliente.getItemCount() > 0) cbCliente.setSelectedIndex(0);
        if (cbEstado.getItemCount() > 0) cbEstado.setSelectedIndex(0);
        if (cbProducto.getItemCount() > 0) cbProducto.setSelectedIndex(0);
    }

