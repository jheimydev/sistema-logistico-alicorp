    private void btnAgregarLineaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarLineaActionPerformed

        if (cbProducto.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "No hay productos disponibles.");
            return;
        }

        String codProducto = (String) cbProducto.getSelectedItem();

        int cantidad;
        try {
            cantidad = Integer.parseInt(txtCantidadLinea.getText().trim());
            if (cantidad <= 0) {
                JOptionPane.showMessageDialog(this, "La cantidad debe ser mayor a 0.");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese una cantidad válida.");
            return;
        }

        double precio = 0;
        String nombreProducto = codProducto;
        for (Producto p : productos) {
            if (p.getCodProducto().equals(codProducto)) {
                precio = p.getPrecio();
                nombreProducto = p.getNombre();
                break;
            }
        }

        int stockDisponible = inventarioController.obtenerStockPorProducto(codProducto);
        int yaEnCarrito = 0;
        for (DetallePedido d : lineasPendientes) {
            if (d.getFkCodProducto().equals(codProducto)) {
                yaEnCarrito += d.getCantidad();
            }
        }

        if (yaEnCarrito + cantidad > stockDisponible) {
            JOptionPane.showMessageDialog(this,
                    "Stock insuficiente para " + nombreProducto + ".\n"
                    + "Disponible: " + stockDisponible + " | En este pedido: " + yaEnCarrito);
            return;
        }

        DetallePedido linea = new DetallePedido(codProducto, cantidad, precio);
        lineasPendientes.add(linea);

        DefaultTableModel modelo = (DefaultTableModel) tblDetalleNuevo.getModel();
        modelo.addRow(new Object[]{nombreProducto, cantidad, precio, linea.getSubtotal()});

        txtCantidadLinea.setText("");
        actualizarMontoTotal();

    }//GEN-LAST:event_btnAgregarLineaActionPerformed

    private void btnGuardarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarPedidoActionPerformed

        if (cbCliente.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Seleccione un cliente.");
            return;
        }

        if (lineasPendientes.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Agregue al menos un producto al pedido.");
            return;
        }

        int idCliente;
        try {
            String seleccion = (String) cbCliente.getSelectedItem();
            idCliente = Integer.parseInt(seleccion.split(" - ")[0].trim());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Cliente inválido.");
            return;
        }

        Date fechaSql;
        try {
            fechaSql = Date.valueOf(txtFecha.getText().trim());
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, "Formato de fecha inválido. Use yyyy-MM-dd.");
            return;
        }

        String estado = (String) cbEstado.getSelectedItem();

        boolean respuesta = controller.guardarPedidoCompleto(idCliente, fechaSql, estado, lineasPendientes);

        if (respuesta) {
            JOptionPane.showMessageDialog(this, "Pedido registrado correctamente.");
            limpiarFormulario();
            listarPedidos();
        } else {
            JOptionPane.showMessageDialog(this, "Error al registrar el pedido.");
        }

    }//GEN-LAST:event_btnGuardarPedidoActionPerformed

