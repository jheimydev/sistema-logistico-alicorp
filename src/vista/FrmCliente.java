    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        if (idClienteOriginal == 0) {
            JOptionPane.showMessageDialog(this, "Seleccione un cliente de la tabla antes de editar.");
            return;
        }

        int idNuevo;
        try {
            idNuevo = Integer.parseInt(txtId.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese un ID numérico válido.");
            return;
        }

        String nombre = txtNombre.getText();
        String tipo = cbTipo.getSelectedItem().toString();
        String contacto = txtContacto.getText();

boolean respuesta = controller.editarCliente(idClienteOriginal, idNuevo, nombre, tipo, contacto);

if (respuesta) {
    JOptionPane.showMessageDialog(this, "Cliente actualizado correctamente");
    listarClientes();
    btnNuevoActionPerformed(null);
} else {
    JOptionPane.showMessageDialog(this, "Error al actualizar el cliente. Verifique que el nuevo ID no esté repetido y que el cliente no tenga pedidos asociados.");
}
        
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        if (idClienteOriginal == 0) {
            JOptionPane.showMessageDialog(this, "Seleccione un cliente de la tabla antes de eliminar.");
            return;
        }

        int opcion = JOptionPane.showConfirmDialog(
        this,
        "¿Está seguro de eliminar este cliente?",
        "Confirmar",
        JOptionPane.YES_NO_OPTION);

if (opcion == JOptionPane.YES_OPTION) {

    boolean respuesta = controller.eliminarCliente(idClienteOriginal);

    if (respuesta) {

        JOptionPane.showMessageDialog(this, "Cliente eliminado correctamente");

        listarClientes();
        btnNuevoActionPerformed(null);

    } else {

        JOptionPane.showMessageDialog(this, "Error al eliminar el cliente");

    }

}
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
       
        int fila = tblClientes.getSelectedRow();

        idClienteOriginal = Integer.parseInt(tblClientes.getValueAt(fila, 0).toString());
        txtId.setText(tblClientes.getValueAt(fila, 0).toString());
        txtNombre.setText(tblClientes.getValueAt(fila, 1).toString());
        cbTipo.setSelectedItem(tblClientes.getValueAt(fila, 2).toString());
        txtContacto.setText(tblClientes.getValueAt(fila, 3).toString());
        
    }//GEN-LAST:event_tblClientesMouseClicked

    private void listarClientes() {

    DefaultTableModel modelo = (DefaultTableModel) tblClientes.getModel();
    modelo.setRowCount(0);

    List<Cliente> lista = controller.listarClientes();

    for (Cliente cliente : lista) {
        modelo.addRow(new Object[]{
            cliente.getIdCliente(),
            cliente.getNombre(),
            cliente.getTipo(),
            cliente.getContacto()
        });
    }
}
    
    
