    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
       
        ClienteController controller = new ClienteController();

int id;
try {
    id = Integer.parseInt(txtId.getText().trim());
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this, "Ingrese un ID numérico válido.");
    return;
}

String nombre = txtNombre.getText();
String tipo = cbTipo.getSelectedItem().toString();
String contacto = txtContacto.getText();

boolean respuesta = controller.guardarCliente(id, nombre, tipo, contacto);

if (respuesta) {
    JOptionPane.showMessageDialog(this, "Cliente registrado correctamente");

    txtId.setText("");
    txtNombre.setText("");
    txtContacto.setText("");
    cbTipo.setSelectedIndex(0);

    listarClientes();   // <-- Agrega esta línea

} else {

    JOptionPane.showMessageDialog(this, "Error al registrar cliente. Verifique que el ID no esté repetido.");

}
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed

